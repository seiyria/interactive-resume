<p>
This game, if you can call it that (I do!), is all about watching numbers get larger and having (little to) nothing to do with them going up! The name of the game is Word Soup, but it's really just a clever ruse for an idling game. <i>But why is it called Word Soup</i>? Well thank you for asking. Let me tell you! This game has a lot of random events. <i>A lot</i>. Your equipment is also randomly generated. These names and events are chosen from a carefully written set of lists located somewhere, and the people who wrote them thought only of amusement when they were writing these lists! Therefore, I estimate that you will have an amusement level of over nine thousand at some point during the course of this game. 
</p>
<p>
Over the course of the game, many things can happen. You may fight side-by-side with your fellow players, you may fight against your fellow players, you may engage monsters in combat, you may find gold, you may get kicked in the shin by a brutal child. There are a few core aspects of the game to cover, here:
<dl>
<dt><h4>Character Information</h4></dt>
<dd>Characters have no stats, but instead their stats are made up by their equipment (explained later). Characters also come to find money throughout the game (also explained later). Characters also have a name, a class, an alignment, a location, and a time to level. Your name and class are chosen when you create your character (see the Commands header below). Your time to level goes down every second you're playing the game. When it hits zero, you level up. Your alignment changes a lot, but you can also choose to set it yourself (see the Commands header below). Your location is not something you directly interact with. Eventually there will be a map players can look at, which displays all monsters and players on the map.</dd>
<dt><h4>Events</h4></dt>
<dd>All sorts of events can happen in the game. Most often, they will affect your primary stats (money, equipment, and time to level). Sometimes they affect your location, and sometimes it can affect other things that I'm not telling you about!</dd>
<dt><h4>Equipment</h4></dt>
<dd>Equipment determines your overall "score." Your score is a combination of your physical attack, magical attack, emotional attack, and spiritual attack. All of these will be explained in the damage types section.
<dt><h4>Money</h4><dt>
<dd>You can use money to buy items in the shop. Yes, you aren't idling, and no, the money doesn't get spent on items automatically.</dd>
<dt><h4>Combat</h4><dt>
<dd>Combat is the trickiest part of the game to explain. I'll start by saying this: you do not control anything about combat. Now that the initial shock is gone, we can go on to explain other things. <br />
Every combatant has a score, which ultimately is their health in combat. Score is determined by totalling your physical attack, magical attack, emotional attack, and spiritual attack. Each combatant gets a turn to deal damage to the opposing side. Whoever doesn't die first, wins.</dd>
<dt><h4>Damage Types</h4></dt>
<dd>Physical - This type of damage is rolled on when a combatant has to do a normal attack. </dd>
<dd>Magical - This type of damage is rolled on when a combatant has to do a magical attack.</dd>
<dd>Emotional - This type of damage is done at the start of every battle, and only done once.</dd>
<dd>Spiritual - This type of damage is done at the end of every battle round.</dd>
<dt><h4>Alignment</h4></dt>
<dd>There are three alignments: good, neutral, and evil. Each of them does something different, and no one alignment is the best.</dd>
