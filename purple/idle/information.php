<h3>Penalty Calculation</h3>
<p>
Penalties happen when you interact with the game, for the most part. These are always time-related, and denoted as pN, where N is the factor of time you get added to your time to level, relative to your level. This means that as your level goes up, these penalties increase also. A brief chart of non-command penalties follows:
